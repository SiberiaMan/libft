#include "ft_printf.h"

int					ft_atoif(char *str, int *i)
{
	int     res;
	int     fl;

	res = 0;
	fl = 1;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			fl = -1;
		str++;
	}
	while ('0' <= *str && *str <= '9')
	{
		(*i)++;
		res = res * 10 + *str - '0';
		str++;
    }
	return (res * fl);
}
